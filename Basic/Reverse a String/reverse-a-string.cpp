class Solution
{
    public:
    string reverseWord(string str)
    {
      int Start=0,End=str.length()-1;
      while(Start<=End){
          int temp=str[Start];
          str[Start]=str[End];
          str[End]=temp;
          Start++;
          End--;
      }
    return str;
    }
};



